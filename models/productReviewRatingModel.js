import mongoose, { Schema } from "mongoose";

const productReviewRatingSchema = new Schema(
  {
    productId: {
      type: Schema.Types.ObjectId,
      ref: "Product",
    },
    userId: {
      type: Schema.Types.ObjectId,
      ref: "User",
    },
    rating: {
      type: Number,
      min: 1,
      max: 5,
    },
    review: {
      type: String,
    },
  },
  {
    timestamps: true,
  }
);

export const productReviewRatingModel = mongoose.model(
  "ProductReviewRating",
  productReviewRatingSchema
);
